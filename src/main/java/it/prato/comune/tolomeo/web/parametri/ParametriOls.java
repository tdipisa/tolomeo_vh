package it.prato.comune.tolomeo.web.parametri;

public class ParametriOls
{
	private String	url;
	private String	username;
	private String	password;
	private String	zoomLevel	= "5000";
	private boolean	useProxyServlet = false;
	private boolean conGeocoding = false;

	public String getUrl()
	{
		return url;
	}
	
	public void setUrl(String host)
	{
		this.url = host;
	}
	
	public String getUsername()
	{
		return username;
	}
	
	public void setUsername(String username)
	{
		this.username = username;
	}
	
	public String getPassword()
	{
		return password;
	}
	
	public void setPassword(String password)
	{
		this.password = password;
	}
	
	public String getZoomLevel()
	{
		return zoomLevel;
	}
	
	public void setZoomLevel(String zoomLevel)
	{
		this.zoomLevel = zoomLevel;
	}
	
	public boolean isUseProxyServlet()
	{
		return useProxyServlet;
	}

	public void setUseProxyServlet(boolean useProxyServlet)
	{
		this.useProxyServlet = useProxyServlet;
	}

	/**
	 * Recupera conGeocoding.
	 *
	 * @return conGeocoding
	 */
	public boolean isConGeocoding() {
		return conGeocoding;
	}

	/**
	 * Imposta il valore di conGeocoding.
	 * @param conGeocoding valore di conGeocoding da impostare
	 */
	public void setConGeocoding(boolean conGeocoding) {
		this.conGeocoding = conGeocoding;
	}
}
